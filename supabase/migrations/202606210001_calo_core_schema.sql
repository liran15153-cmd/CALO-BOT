insert into storage.buckets (id, name, public)
values ('meal-images', 'meal-images', false)
on conflict (id) do update set public = excluded.public;

drop policy if exists meal_images_insert_own_folder on storage.objects;
create policy meal_images_insert_own_folder on storage.objects
for insert to authenticated
with check (
  bucket_id = 'meal-images'
  and (storage.foldername(name))[1] = (select auth.uid())::text
);

drop policy if exists meal_images_select_own_folder on storage.objects;
create policy meal_images_select_own_folder on storage.objects
for select to authenticated
using (
  bucket_id = 'meal-images'
  and (storage.foldername(name))[1] = (select auth.uid())::text
);

drop policy if exists meal_images_delete_own_folder on storage.objects;
create policy meal_images_delete_own_folder on storage.objects
for delete to authenticated
using (
  bucket_id = 'meal-images'
  and (storage.foldername(name))[1] = (select auth.uid())::text
);
