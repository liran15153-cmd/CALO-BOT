create table if not exists public.usage_events (
  id bigint generated by default as identity primary key,
  user_id bigint references public.users(id) on delete cascade,
  usage_date date not null,
  task varchar(60) not null,
  provider varchar(60) not null,
  model varchar(120),
  estimated_tokens_in integer not null default 0,
  estimated_tokens_out integer not null default 0,
  token_breakdown_json jsonb not null default '{}'::jsonb,
  cost_estimate double precision,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.pending_actions (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  session_id bigint references public.chat_sessions(id) on delete set null,
  action_type varchar(80) not null,
  status varchar(40) not null default 'pending',
  subject_type varchar(80) not null,
  subject_id bigint not null,
  payload_json jsonb not null default '{}'::jsonb,
  resolution varchar(40),
  created_from_message_id bigint references public.chat_messages(id) on delete set null,
  resolved_by_message_id bigint references public.chat_messages(id) on delete set null,
  resolved_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

