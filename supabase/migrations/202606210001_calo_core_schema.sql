create table if not exists public.users (
  id bigint generated by default as identity primary key,
  auth_user_id uuid unique,
  email text,
  name varchar(120) not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.fitness_profiles (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  age_range varchar(40),
  gender varchar(40),
  height_cm double precision,
  weight_kg double precision,
  main_goal varchar(80) not null,
  experience_level varchar(40) not null,
  training_location varchar(40) not null,
  available_equipment jsonb not null default '[]'::jsonb,
  weekly_availability integer not null,
  session_length_minutes integer not null,
  preferred_workout_days jsonb not null default '[]'::jsonb,
  injuries_limitations text,
  nutrition_preference varchar(80),
  foods_disliked text,
  allergies text,
  typical_schedule text,
  coaching_style varchar(40) not null default 'direct',
  consent_disclaimer boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(user_id)
);

create table if not exists public.chat_sessions (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  title varchar(160) not null default 'Coach chat',
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.chat_messages (
  id bigint generated by default as identity primary key,
  session_id bigint not null references public.chat_sessions(id) on delete cascade,
  user_id bigint not null references public.users(id) on delete cascade,
  role varchar(20) not null,
  content text not null,
  metadata_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.workout_plans (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  name varchar(160) not null,
  goal varchar(120) not null,
  duration_weeks integer not null default 4,
  days_per_week integer not null,
  equipment_needed jsonb not null default '[]'::jsonb,
  plan_json jsonb not null default '{}'::jsonb,
  progression_rule text,
  recovery_note text,
  is_current boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.workouts (
  id bigint generated by default as identity primary key,
  plan_id bigint references public.workout_plans(id) on delete cascade,
  user_id bigint not null references public.users(id) on delete cascade,
  name varchar(160) not null,
  scheduled_day varchar(40),
  difficulty varchar(40),
  workout_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.workout_exercises (
  id bigint generated by default as identity primary key,
  workout_id bigint not null references public.workouts(id) on delete cascade,
  name varchar(160) not null,
  sets varchar(40),
  reps_or_duration varchar(80),
  rest varchar(80),
  notes text,
  alternatives jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.workout_logs (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  workout_id bigint references public.workouts(id) on delete set null,
  logged_on date not null,
  status varchar(40) not null,
  exercise_results jsonb not null default '[]'::jsonb,
  rpe integer,
  notes text,
  pain_flag boolean not null default false,
  parse_confidence varchar(20) not null default 'medium',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.meal_logs (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  eaten_on date not null,
  meal_type varchar(40),
  note text,
  image_path varchar(500),
  calories_min integer,
  calories_max integer,
  protein_min integer,
  protein_max integer,
  carbs_min integer,
  carbs_max integer,
  fat_min integer,
  fat_max integer,
  confidence varchar(20),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.meal_items (
  id bigint generated by default as identity primary key,
